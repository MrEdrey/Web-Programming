<?php
$multiplier = 2;
    
fscanf (STDIN, "%f", $a);
fscanf (STDIN, "%f", $b);

if ($a>=$b){
    $value1 = $a;
    $value2 = $b;
}else{
    $value1 = $b;
    $value2 = $a;
}if ($value1 % $value2 == 0) {
    echo (1 + ($value1 / $value2))."\n";
}else{
    while(true){
        if ($value1*$multiplier % $value2 == 0){
            echo ($multiplier + (($value1 * $multiplier) / $value2)) . "\n";
            break;
        }$multiplier += 1;
    }
}
?>